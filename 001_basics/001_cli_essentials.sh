#!/usr/bin/env bash

# --help flag all distributions

clear # clear the screen

cd <path> # change directory

pwd # working directory

ls | ls <path> # list

cp <src path> <dest path> # copy

mv <src path> <dest path># move

rm <path> # remove 

echo "Message" # print

cat <path> # displays text, new file, read file, modify file and file concatenation

less <path> # displays 1 page at a time

[cmd] | grep "text" # search text or searches the given file

mkdir <path> # make directory

touch <path> # creates file

chmod <permission> <path> # modify the file permission [user group everybody-else]

man <cmd> # manual
